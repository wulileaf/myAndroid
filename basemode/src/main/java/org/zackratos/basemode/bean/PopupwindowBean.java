package org.zackratos.basemode.bean;

// 底部弹出框对应的bean文件
public class PopupwindowBean {

    private String name;
    private String value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
